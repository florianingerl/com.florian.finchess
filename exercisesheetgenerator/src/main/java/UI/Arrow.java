package UI;

import java.awt.Color;

public class Arrow {

	public String from;
	public String to;
	public Color color;
	
	public Arrow(String from, String to, Color color) {
		this.from = from;
		this.to = to;
		this.color = color;
	}

	public Arrow(String from, String to) {
		this.from = from;
		this.to = to;
	}
	
	
}
